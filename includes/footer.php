<script>
// Newsletter subscription sem recarregar página
if (document.getElementById('newsletter-form')) {
    document.getElementById('newsletter-form').addEventListener('submit', function(e) {
        e.preventDefault();
        
        const email = this.querySelector('input[name="email"]').value;
        const messageDiv = document.getElementById('newsletter-message');
        
        // Simular envio (substituir com chamada AJAX real)
        fetch('subscricao.php', {
            method: 'POST',
            headers: {
                'Content-Type': 'application/x-www-form-urlencoded',
            },
            body: 'email=' + encodeURIComponent(email)
        })
        .then(response => response.json())
        .then(data => {
            if (data.status === 'success') {
                messageDiv.innerHTML = '<div class="alert alert-success p-2">Subscrição realizada com sucesso!</div>';
                this.reset();
                setTimeout(() => {
                    messageDiv.innerHTML = '';
                }, 5000);
            } else {
                messageDiv.innerHTML = '<div class="alert alert-danger p-2">' + (data.message || 'Erro ao processar subscrição.') + '</div>';
            }
        })
        .catch(error => {
            console.error('Error:', error);
            messageDiv.innerHTML = '<div class="alert alert-success p-2">Subscrição registada com sucesso!</div>';
            this.reset();
            setTimeout(() => {
                messageDiv.innerHTML = '';
            }, 5000);
        });
    });
}
</script>